const Dancefloor = require("./Dancefloor")

class Command {

    /**
     * 
     * @param {Dancefloor} client 
     * @param {JSON} ops 
     */
    constructor(client, ops = {}) {
        this.client = client;
        this._patch(ops);
    }

    _patch(ops = {}) {
        this.help = {
            name: ops.name || null,
            description: ops.description || "No description provided!",
            aliases: ops.aliases || [],
            category: "Others"
        };

        this.name = this.help.name;
    }

}

module.exports = Command;